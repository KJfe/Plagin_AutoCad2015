namespace Model.Colour
{
    /// <summary>
    /// Класс реализующий палитру ARGB
    /// </summary>
    public class ColorArgb
    {

        /// <summary>
        /// Значение прозрачности
        /// </summary>
        public byte A { get; set; }
        /// <summary>
        /// Значение красного цвета
        /// </summary>
        public byte R { get; set; }
        /// <summary>
        /// Значение зеленого цвета
        /// </summary>
        public byte G { get; set; }
        /// <summary>
        /// Значение синиго цвета
        /// </summary>
        public byte B { get; set; }
    }
}
